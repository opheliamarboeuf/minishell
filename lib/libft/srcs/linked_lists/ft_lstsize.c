#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		len;
	t_list	*temp;

	len = 0;
	temp = lst;
	if (!lst)
		return (0);
	while (temp)
	{
		len++;
		temp = temp->next;
	}
	return (len);
}
